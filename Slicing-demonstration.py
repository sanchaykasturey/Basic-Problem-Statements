##the basic program to demonstrate slicing
list = ["q","r","s","t","u","v","w","x","y","z"]
first5 = slice(5)#slicing the first 5
print(list[first5])#output
middle3 = slice(4,7)#slicing specifically in between
print(list[middle3])#output
last = slice(9,10)#slicing the last elements
print(list[last])#output
